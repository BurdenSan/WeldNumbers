    private void jFileChooser1ActionPerformed(java.awt.event.ActionEvent evt) throws IOException {                                              
           // TODO add your handling code here:
           File file = jFileChooser1.getSelectedFile();
           HashMap <String, WeldNumber> newMap = LineReader.readLineBR(file.getAbsolutePath(),robotNumber);
           //first calls the readLineBR method to load the contents of the file into a HashMap
           for (int i=0; i<newMap.size(); i++){
            Collection <WeldNumber> addWelds = newMap.values();
            Iterator <WeldNumber> it = addWelds.iterator();
            WeldNumber weldToBeAdded = new WeldNumber();
            while (it.hasNext()){
                weldToBeAdded =  it.next();
                DataSaver.fileMaker(robotNumber, userCellName, weldToBeAdded.getWeldNum(),userFrameStyle);
                DataSaver.serializerSave(weldToBeAdded);
            }            
           }                    
           CardLayout card = (CardLayout)mainPanel.getLayout();
           card.show(mainPanel, "card5");		
		
    }                                             

