            userFrameStyle = userPickFrameStyle.getSelectedValue();
            WeldNumber foundWeld = DataSaver.supplyRetrieveSave(userCellName, userWeldNumber, userFrameStyle);            
            cellNameLabel.setText(userCellName);
            weldNumLabel.setText(userWeldNumber);
            frameStyleLabelSRLabel.setText(userFrameStyle);
