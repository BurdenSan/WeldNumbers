    private void filterCellNameKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_filterCellNameKeyTyped
        // TODO add your handling code here:
        String filterKey;
        filterKey = filterCellName.getText();        
        int indices = mod.getSize();        
        String [] unfilteredList = new String [indices];
        for (int i=0; i<indices; i++){
            unfilteredList [i] = mod.getElementAt(i);
        }
        String [] filteredList = new String [unfilteredList.length];
        int addedStringCounter = 0;
        for (String addToFilter : unfilteredList){
            if (addToFilter.toLowerCase().contains(filterKey.toLowerCase())){
               filteredList [addedStringCounter] = addToFilter;
               addedStringCounter++;
            }
        }
        DefaultListModel <String> dLM = new DefaultListModel <String> ();
        for (String addToModel : filteredList){
            if (addToModel != null){
                dLM.addElement(addToModel);
            }
        }
        userPickCellName.setModel(dLM);       
        
    }//GEN-LAST:event_filterCellNameKeyTyped

